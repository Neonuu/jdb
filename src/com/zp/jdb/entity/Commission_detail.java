package com.zp.jdb.entity;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 用户佣金交易记录表
 * @author Administrator
 *
 */
public class Commission_detail {
	private Long id;
	private Long user_id;
	private String state;//佣金类型：返佣，提现，推荐人返佣
	private BigDecimal change_amount;
	private BigDecimal balance;
	private Date add_time;
	private String bank_number;
	private Byte id_del;
	private int mark;
	private String reason;
	private String user_name;
	private String strAdd_time;
	private String phone;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getUser_id() {
		return user_id;
	}
	public void setUser_id(Long user_id) {
		this.user_id = user_id;
	}
	public BigDecimal getChange_amount() {
		return change_amount;
	}
	public void setChange_amount(BigDecimal change_amount) {
		this.change_amount = change_amount;
	}
	public BigDecimal getBalance() {
		return balance;
	}
	public void setBalance(BigDecimal balance) {
		this.balance = balance;
	}
	public Date getAdd_time() {
		return add_time;
	}
	public void setAdd_time(Date add_time) {
		this.add_time = add_time;
	}
	public String getBank_number() {
		return bank_number;
	}
	public void setBank_number(String bank_number) {
		this.bank_number = bank_number;
	}
	public Byte getId_del() {
		return id_del;
	}
	public void setId_del(Byte id_del) {
		this.id_del = id_del;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public int getMark() {
		return mark;
	}
	public void setMark(int mark) {
		this.mark = mark;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getStrAdd_time() {
		return strAdd_time;
	}
	public void setStrAdd_time(String strAdd_time) {
		this.strAdd_time = strAdd_time;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	

	
}
