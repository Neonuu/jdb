package com.zp.jdb.entity;

import com.zp.jdb.util.pageValue;

/**
 * 短信模板实体类
 *	
 */
public class Sms_msg {
	private int tid;
	@Override
	public String toString() {
		return tid + " " 
		+ sms_msg + " " + sms_type
		+ " ";
	}
	//短信模板
	private String sms_msg;
	
	//短信类型
	private String sms_type;
	
	//页码
	private Integer pagenum;
	
	//起始下标
	private Integer startIndex;
	
	//每页几条
	private Integer pagecount = pageValue.PAGE_COUNT;
	
	//总条数
	private Integer totalCount;
	
	//最大页码
	private Integer maxpage;
		
	public Integer getPagenum() {
		return pagenum;
	}
	public void setPagenum(Integer pagenum) {
		this.pagenum = pagenum;
	}
	public Integer getStartIndex() {
		return startIndex;
	}
	public void setStartIndex(Integer startIndex) {
		this.startIndex = startIndex;
	}
	public Integer getPagecount() {
		return pagecount;
	}
	public void setPagecount(Integer pagecount) {
		this.pagecount = pagecount;
	}
	public Integer getTotalCount() {
		return totalCount;
	}
	public void setTotalCount(Integer totalCount) {
		this.totalCount = totalCount;
	}
	public Integer getMaxpage() {
		return maxpage;
	}
	public void setMaxpage(Integer maxpage) {
		this.maxpage = maxpage;
	}
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getSms_msg() {
		return sms_msg;
	}
	public void setSms_msg(String sms_msg) {
		this.sms_msg = sms_msg;
	}
	public String getSms_type() {
		return sms_type;
	}
	public void setSms_type(String sms_type) {
		this.sms_type = sms_type;
	}

	
	
	
}
