package com.zp.jdb.entity;

import com.zp.jdb.util.ConstantValue;

public class User {
	
	private Integer user_id;
	
	private String user_name;
	
	private String password;
	
	private String phoneNumber;
	
	private String image_address;
	
	private String email;
	
	private String province;
	
	private String city;
	
	private int user_status;
	
	private int limit;
	
	private int isdel;
	
	private Integer orgId;
	
	//页码
	private Integer pagenum;
	
	//起始下标
	private Integer startIndex;
	
	//每页几条
	private Integer pagecount = ConstantValue.PAGE_COUNT;
	
	//总条数
	private Integer totalCount;
	
	//最大页码
	private Integer maxpage;

	public Integer getUser_id() {
		return user_id;
	}

	public void setUser_id(Integer userId) {
		user_id = userId;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String userName) {
		user_name = userName;
	}

	public Integer getOrgId() {
		return orgId;
	}

	public void setOrgId(Integer orgId) {
		this.orgId = orgId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getImage_address() {
		return image_address;
	}

	public void setImage_address(String imageAddress) {
		image_address = imageAddress;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public Integer getUser_status() {
		return user_status;
	}

	public void setUser_status(Integer userStatus) {
		user_status = userStatus;
	}

	public int getLimit() {
		return limit;
	}

	public void setLimit(int limit) {
		this.limit = limit;
	}

	public int getIsdel() {
		return isdel;
	}

	public void setIsdel(int isdel) {
		this.isdel = isdel;
	}

	public Integer getPagenum() {
		return pagenum;
	}

	public void setPagenum(Integer pagenum) {
		this.pagenum = pagenum;
	}

	public Integer getStartIndex() {
		return startIndex;
	}

	public void setStartIndex(Integer startIndex) {
		this.startIndex = startIndex;
	}

	public Integer getPagecount() {
		return pagecount;
	}

	public void setPagecount(Integer pagecount) {
		this.pagecount = pagecount;
	}

	public Integer getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(Integer totalCount) {
		this.totalCount = totalCount;
	}

	public Integer getMaxpage() {
		return maxpage;
	}

	public void setMaxpage(Integer maxpage) {
		this.maxpage = maxpage;
	}

	public void setUser_status(int userStatus) {
		user_status = userStatus;
	}
	
}

